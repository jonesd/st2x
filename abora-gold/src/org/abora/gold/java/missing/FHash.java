	private static int ilogb(double value) {
		if (value == 0.0) {
			return Integer.MIN_VALUE;
		} else if (Double.isNaN(value) || Double.isInfinite(value)) {
			return Integer.MAX_VALUE;
		} else {
			long bits = Double.doubleToLongBits(value);
			return (int)((bits & 0x7ff0000000000000L) >> 52);
		}
	}

	public static int fastHash(String s) {
		//TODO definitely not unicode safe!
	    int result = 0;
	    for (int i = 0; i < s.length(); i++) {
	    		int stepValue = s.charAt(i);
	    		result = hashSBoxes[i & 7][stepValue] ^ result;
	    }
	    return result;

/* fhashx.cxx	    
	    UInt32 fastHash (char * string) {
	        UInt32 result = 0;
	        for (Int32 i = 0; *string; i++) {
	    	result = hashSBoxes[i & 7][*string++] ^ result;
	        }
	        return result;
	    }
*/
	}
	
	public static int fastHashString(String prob) {
		return fastHash(prob);
	}
	
